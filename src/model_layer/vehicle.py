
class Vehicle:
    def __init__(self, manufacturer, model, vehicle_type, status, man_year, color, license_type, location, id_number):
        self.manufacturer = manufacturer
        self.model = model
        self.type = vehicle_type
        self.status = status
        self.year = man_year
        self.color = color
        self.license_type = license_type
        self.location = location
        self.id = id_number